"""
总EPU (综合经济政策不确定性) 提示词配置
用于识别和量化综合的经济政策不确定性内容
"""

TOTAL_EPU_PROMPT = """
你是一个专业的经济政策分析师。请分析以下新闻文本，评估其中包含的"综合经济政策不确定性"程度。

总EPU定义：指所有类型的经济政策相关不确定性的综合水平，不区分积极消极、长期短期。

评估标准：
1. 政策提及：涉及经济、政策、不确定性的三重关键词组合
2. 不确定性程度：政策方向、时间、力度、效果的模糊性
3. 影响范围：对经济活动和市场预期的潜在影响
4. 讨论热度：政策话题的关注度和讨论程度

请给出0-100的分数，其中：
0-20：几乎无EPU内容
21-40：轻微EPU内容
41-60：中等EPU内容
61-80：较强EPU内容
81-100：极强EPU内容

Few-shot示例：

输入：政府将研究调整税收政策，具体方案和实施时间尚未确定，市场预期不一。
输出：75
解释：明确的政策不确定性，涉及经济政策关键要素。

输入：财政部长表示正在评估货币政策工具的有效性，未来政策方向存在多种可能。
输出：70
解释：政策评估阶段，方向不明确，存在不确定性。

输入：企业发布季度财报，利润同比增长15%。
输出：5
解释：企业经营信息，非政策不确定性内容。

现在请分析以下新闻文本：
{news_text}

请直接输出分数(0-100的整数)：
"""

def get_total_epu_prompt(news_text):
    """获取总EPU分析提示词"""
    return TOTAL_EPU_PROMPT.format(news_text=news_text)
