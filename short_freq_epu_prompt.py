"""
短期高频EPU (短期高频经济政策不确定性) 提示词配置
用于识别和量化短期内高频变化的经济政策不确定性内容
"""

SHORT_FREQ_EPU_PROMPT = """
你是一个专业的经济政策分析师。请分析以下新闻文本，评估其中包含的"短期高频经济政策不确定性"程度。

短期高频EPU定义：指在短期内(数天到数月)可能发生变化、频繁调整的经济政策相关不确定性。

评估标准：
1. 时效性：政策变化的时间窗口短，影响即时
2. 频率：政策调整频繁，变化密集
3. 市场反应：引起短期市场波动和即时关注
4. 政策工具：货币政策、短期调控措施、临时性政策

请给出0-100的分数，其中：
0-20：几乎无短期高频EPU内容
21-40：轻微短期高频EPU内容
41-60：中等短期高频EPU内容
61-80：较强短期高频EPU内容
81-100：极强短期高频EPU内容

Few-shot示例：

输入：央行今日进行逆回购操作，投放流动性，市场利率即时下行。
输出：75
解释：货币政策工具的即时操作，短期高频特征明显。

输入：监管层紧急发布股市熔断机制调整通知，将于明日生效。
输出：85
解释：紧急政策调整，即时生效，高频特征突出。

输入：政府发布五年发展规划纲要。
输出：15
解释：长期规划，非短期高频政策。

现在请分析以下新闻文本：
{news_text}

请直接输出分数(0-100的整数)：
"""

def get_short_freq_epu_prompt(news_text):
    """获取短期高频EPU分析提示词"""
    return SHORT_FREQ_EPU_PROMPT.format(news_text=news_text)
