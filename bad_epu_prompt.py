"""
向坏EPU (消极经济政策不确定性) 提示词配置
用于识别和量化消极的经济政策不确定性相关内容
"""

BAD_EPU_PROMPT = """
你是一个专业的经济政策分析师。请分析以下新闻文本，评估其中包含的"向坏的经济政策不确定性"程度。

向坏EPU定义：指政策变化可能带来消极经济影响，或政策方向不明确导致市场担忧的不确定性情况。

评估标准：
1. 消极政策信号：加税、收紧管制、限制措施等
2. 政策担忧：市场对不利政策的担心和讨论
3. 政策冲突：政策方向不一致、政策制定者意见分歧
4. 不确定性：负面政策风险、实施的模糊性

请给出0-100的分数，其中：
0-20：几乎无向坏EPU内容
21-40：轻微向坏EPU内容
41-60：中等向坏EPU内容
61-80：较强向坏EPU内容
81-100：极强向坏EPU内容

Few-shot示例：

输入：监管部门可能出台更严格的金融监管政策，具体措施仍在讨论中。
输出：70
解释：监管收紧信号(消极)，具体措施不确定。

输入：贸易摩擦升级，关税政策存在进一步恶化可能。
输出：80
解释：明确的负面政策风险和高度不确定性。

输入：今日股市上涨，投资者情绪乐观。
输出：0
解释：与经济政策不确定性无关。

现在请分析以下新闻文本：
{news_text}

请直接输出分数(0-100的整数)：
"""

def get_bad_epu_prompt(news_text):
    """获取向坏EPU分析提示词"""
    return BAD_EPU_PROMPT.format(news_text=news_text)
