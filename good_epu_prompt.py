"""
向好EPU (积极经济政策不确定性) 提示词配置
用于识别和量化积极的经济政策不确定性相关内容
"""

GOOD_EPU_PROMPT = """
你是一个专业的经济政策分析师。请分析以下新闻文本，评估其中包含的"向好的经济政策不确定性"程度。

向好EPU定义：指政策变化可能带来积极经济影响，但具体实施时间、力度、效果仍存在不确定性的情况。

评估标准：
1. 积极政策信号：减税、放松管制、促增长措施等
2. 政策预期：市场对利好政策的期待和讨论
3. 改革举措：结构性改革、开放措施等积极变化
4. 不确定性：政策具体细节、时间表、影响范围的模糊性

请给出0-100的分数，其中：
0-20：几乎无向好EPU内容
21-40：轻微向好EPU内容
41-60：中等向好EPU内容
61-80：较强向好EPU内容
81-100：极强向好EPU内容

Few-shot示例：

输入：政府宣布将出台新的减税政策，但具体减税幅度和实施时间尚未确定。
输出：75
解释：明确的减税政策信号(积极)，但实施细节不确定。

输入：央行表示正在研究降准政策，市场普遍预期将在近期实施。
输出：65
解释：货币政策放松信号，但时间和力度存在不确定性。

输入：今日天气晴朗，适宜户外活动。
输出：0
解释：与经济政策无关。

现在请分析以下新闻文本：
{news_text}

请直接输出分数(0-100的整数)：
"""

def get_good_epu_prompt(news_text):
    """获取向好EPU分析提示词"""
    return GOOD_EPU_PROMPT.format(news_text=news_text)
